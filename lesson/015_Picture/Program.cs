void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++) 
    // matr.GetLength(0) возвращает количество строк в массиве
    {
        for (int j = 0; j < image.GetLength(1); j++)
        // matr.GetLength(1) возвращает количество столбцов в массиве
        {
            // Console.Write($"{image[i, j]} ");
            if (image[i,j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }

        Console.WriteLine();
    }
}

void FillImage (int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row-1, col); // на строчку выше в том же столбце
        FillImage(row, col-1); // в той же строке влево
        FillImage(row+1, col); // вниз в том же столбце
        FillImage(row, col+1); // в строке вправо
    }
}

